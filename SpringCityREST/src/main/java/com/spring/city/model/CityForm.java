package com.spring.city.model;

public class CityForm {

    private String firstCity;
    private String secondCity;
    private String thirdCity;
    private String firstFactor;
    private String secondFactor;
    private String thirdFactor;

    public String getFirstCity() {
        return firstCity;
    }

    public void setFirstCity(String firstCity) {
        this.firstCity = firstCity;
    }

    public String getSecondCity() {
        return secondCity;
    }

    public void setSecondCity(String secondCity) {
        this.secondCity = secondCity;
    }

    public String getThirdCity() {
        return thirdCity;
    }

    public void setThirdCity(String thirdCity) {
        this.thirdCity = thirdCity;
    }

    public String getFirstFactor() {
        return firstFactor;
    }

    public void setFirstFactor(String firstFactor) {
        this.firstFactor = firstFactor;
    }

    public String getSecondFactor() {
        return secondFactor;
    }

    public void setSecondFactor(String secondFactor) {
        this.secondFactor = secondFactor;
    }

    public String getThirdFactor() {
        return thirdFactor;
    }

    public void setThirdFactor(String thirdFactor) {
        this.thirdFactor = thirdFactor;
    }
}
